# MMLU
# ---------------------------------------------------------------------------

def download_mmlu(questions_per_subject: int = 5) -> list[dict]:
    """Download MMLU questions: questions_per_subject × 20 subjects."""
    total = questions_per_subject * len(MMLU_SUBJECTS)
    print(f"\n[MMLU] Downloading {questions_per_subject} questions × "
          f"{len(MMLU_SUBJECTS)} subjects = {total} total...")

    from datasets import load_dataset
    prompts: list[dict] = []

    for subj in MMLU_SUBJECTS:
        ds = load_dataset("cais/mmlu", subj, split="test", streaming=True)
        subj_count = 0
        for row in ds:
            if subj_count >= questions_per_subject:
                break
            question = row["question"]
            choices = row["choices"]  # list of 4 strings
            answer_idx = row["answer"]  # 0-3
            correct_letter = LETTER_MAP[answer_idx]

            choice_lines = "\n".join(
                f"{LETTER_MAP[j]}) {choices[j]}" for j in range(len(choices))
            )
            prompt = (
                f"Question: {question}\n"
                f"{choice_lines}\n"
                f"Answer with only the letter A, B, C, or D:"
            )
            q_num = subj_count + 1
            prompts.append({
                "id": f"mmlu_{subj}_{q_num:03d}",
                "prompt": prompt,
                "answer": correct_letter,
                "answer_type": "choice",
                "category": "mmlu",
                "subject": subj,
            })
            subj_count += 1
        print(f"  {subj}: {subj_count} questions")

    print(f"  Total MMLU: {len(prompts)} questions")
    return prompts


# ---------------------------------------------------------------------------
# TruthfulQA MC1
# ---------------------------------------------------------------------------

def download_truthfulqa(count: int = 100) -> list[dict]:
    """Download TruthfulQA MC1 validation questions."""
    print(f"\n[TruthfulQA] Downloading {count} questions from validation split...")

    from datasets import load_dataset
    ds = load_dataset("truthful_qa", "multiple_choice", split="validation", streaming=True)

    prompts: list[dict] = []
    for i, row in enumerate(ds):
        if i >= count:
            break
        question = row["question"]
        mc1 = row["mc1_targets"]
        choices_all = mc1["choices"]
        labels_all  = mc1["labels"]  # 1 = correct, 0 = incorrect

        correct_idx_full = labels_all.index(1)

        # Handle questions with >4 choices: keep correct + first 3 others
        if len(choices_all) > 4:
            correct_choice = choices_all[correct_idx_full]
            others = [
                (c, l)
                for j, (c, l) in enumerate(zip(choices_all, labels_all))
                if j != correct_idx_full
            ]
            # Take first 3 incorrect choices
            selected_others = others[:3]
            # Put correct at position 0
            final_pairs = [(correct_choice, 1)] + selected_others
            correct_letter = "A"  # correct is remapped to index 0
        else:
            final_pairs = list(zip(choices_all, labels_all))
            correct_letter = LETTER_MAP[correct_idx_full]

        # Limit to 4 choices
        final_pairs = final_pairs[:4]
        choice_lines = "\n".join(
            f"{LETTER_MAP[j]}) {final_pairs[j][0]}" for j in range(len(final_pairs))
        )
        prompt = (
            f"Question: {question}\n"
            f"{choice_lines}\n"
            f"Answer with only the letter A, B, C, or D:"
        )
        prompts.append({
            "id": f"truthfulqa_{i + 1:03d}",
            "prompt": prompt,
            "answer": correct_letter,
            "answer_type": "choice",
            "category": "truthfulqa",
        })

    print(f"  Parsed {len(prompts)} TruthfulQA questions")
    answer_dist = {}
    for p in prompts:
        answer_dist[p["answer"]] = answer_dist.get(p["answer"], 0) + 1
    print(f"  Answer distribution: {dict(sorted(answer_dist.items()))}")
    return prompts


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Download ARC-Challenge, HellaSwag, MMLU, TruthfulQA for quality_eval.py"
    )
    parser.add_argument("--arc-challenge-only", action="store_true")
    parser.add_argument("--hellaswag-only",     action="store_true")
    parser.add_argument("--mmlu-only",          action="store_true")
    parser.add_argument("--truthfulqa-only",    action="store_true")
    parser.add_argument(
        "--force", action="store_true",
        help="Re-download even if output files already exist"
    )
    args = parser.parse_args()

    # If any specific flag is set, only do those; otherwise do all
    any_specific = any([
        args.arc_challenge_only, args.hellaswag_only,
        args.mmlu_only, args.truthfulqa_only,
    ])

    do_arc_challenge = args.arc_challenge_only or not any_specific
    do_hellaswag     = args.hellaswag_only     or not any_specific
    do_mmlu          = args.mmlu_only          or not any_specific
    do_truthfulqa    = args.truthfulqa_only    or not any_specific

    outputs = {
        "arc_challenge": DATA_DIR / "arc_challenge_100.yaml",
        "hellaswag":     DATA_DIR / "hellaswag_100.yaml",
        "mmlu":          DATA_DIR / "mmlu_100.yaml",
        "truthfulqa":    DATA_DIR / "truthfulqa_100.yaml",
    }

    # Skip already-existing files unless --force
    if not args.force:
        for key, path in outputs.items():
            if path.exists():
                print(f"  {key} already exists: {path} (use --force to re-download)")
                if key == "arc_challenge": do_arc_challenge = False
                if key == "hellaswag":     do_hellaswag     = False
                if key == "mmlu":          do_mmlu          = False
                if key == "truthfulqa":    do_truthfulqa    = False

    success = True

    if do_arc_challenge:
        try:
            p = download_arc_challenge(100)
            write_yaml(p, outputs["arc_challenge"],
                       "ARC-Challenge test split — 100 questions, 4-choice (A/B/C/D)")
        except Exception as e:
            print(f"\nERROR downloading ARC-Challenge: {e}", file=sys.stderr)
            import traceback; traceback.print_exc()
            success = False

    if do_hellaswag:
        try:
            p = download_hellaswag(100)
            write_yaml(p, outputs["hellaswag"],
                       "HellaSwag validation split — 100 questions, 4-choice sentence completion")
        except Exception as e:
            print(f"\nERROR downloading HellaSwag: {e}", file=sys.stderr)
            import traceback; traceback.print_exc()
            success = False

    if do_mmlu:
        try:
            p = download_mmlu(5)
            write_yaml(p, outputs["mmlu"],
                       "MMLU — 100 questions (5 per subject × 20 subjects), 4-choice (A/B/C/D)")
        except Exception as e:
            print(f"\nERROR downloading MMLU: {e}", file=sys.stderr)
            import traceback; traceback.print_exc()
            success = False

    if do_truthfulqa:
        try:
            p = download_truthfulqa(100)
            write_yaml(p, outputs["truthfulqa"],
                       "TruthfulQA MC1 validation split — 100 questions, multi-choice")
        except Exception as e:
            print(f"\nERROR downloading TruthfulQA: {e}", file=sys.stderr)
            import traceback; traceback.print_exc()
            success = False

    if success:
        print("\n=== Done ===")
        for key, path in outputs.items():
            if path.exists():
                print(f"  {key}: {path}")
        print("\nNext steps:")
        print("  python3 scripts/quality_eval.py --dataset data/arc_challenge_100.yaml --tag arc_challenge --dry-run Q4_K_M")
        print("  python3 scripts/quality_eval.py --dataset data/hellaswag_100.yaml --tag hellaswag --dry-run Q4_K_M")
        print("  python3 scripts/quality_eval.py --dataset data/mmlu_100.yaml --tag mmlu --dry-run Q4_K_M")
        print("  python3 scripts/quality_eval.py --dataset data/truthfulqa_100.yaml --tag truthfulqa --dry-run Q4_K_M")

    return 0 if success else 1


if __name__ == "__main__":
    raise SystemExit(main())
